#include "vnsLoggers.h"
#include "vnsCrossCorrelationFunctor.h"
#include "otbImage.h"
#include "otbVectorImage.h"
#include "otbImageFileReader.h"
#include "otbImageFileWriter.h"

int
vnsCrossCorrelationFunctorCompileTest(int argc, char * argv[])
{
    if (argc != 1)
    {
        std::cerr << argv[0] << std::endl;
        return EXIT_FAILURE;
    }
    // Configure Logger
    vns::Loggers::GetInstance()->Initialize(argv[0]);
    vns::Loggers::GetInstance()->SetMinLevel(vns::LoggerBase::DEBUG);

    const unsigned int Dimension = 2;
    /** Pixel typedefs */
    typedef double InputPixelType;
    typedef unsigned char InputMaskPixelType;
    typedef double OutputPixelType;

    /** Image typedefs */
    typedef otb::VectorImage<InputPixelType, Dimension> InputImageType;
    typedef otb::Image<InputMaskPixelType, Dimension> InputMaskType;
    typedef otb::ImageFileReader<InputImageType> ReaderType;

    typedef vns::Functor::CrossCorrelationFunctor<InputImageType::PixelType, InputMaskType::PixelType, OutputPixelType> FunctorType;

    return EXIT_SUCCESS;
}
