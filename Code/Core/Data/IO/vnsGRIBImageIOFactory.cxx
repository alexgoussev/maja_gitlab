#include "vnsGRIBImageIOFactory.h"

#include "itkCreateObjectFunction.h"
#include "vnsGRIBImageIO.h"
#include "itkVersion.h"


namespace vns
{

GRIBImageIOFactory::GRIBImageIOFactory()
{
  this->RegisterOverride("otbImageIOBase",
                         "vnsGRIBImageIO",
                         "GRIB Image IO",
                         1,
                         itk::CreateObjectFunction<GRIBImageIO>::New());
}

GRIBImageIOFactory::~GRIBImageIOFactory()
{
}

const char*
GRIBImageIOFactory::GetITKSourceVersion(void) const
{
  return ITK_SOURCE_VERSION;
}

const char*
GRIBImageIOFactory::GetDescription() const
{
  return "GRIB ImageIO Factory, permettant le chargement d'image au format GRIB dans l'OTB";
}

} // end namespace vns

