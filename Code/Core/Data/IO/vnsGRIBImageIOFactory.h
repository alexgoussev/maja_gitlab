#ifndef __vnsGRIBImageIOFactory_h
#define __vnsGRIBImageIOFactory_h

#include "itkObjectFactoryBase.h"
#include "itkImageIOBase.h"

namespace vns
{
/** \class GRIBImageIOFactory
 * \brief Creation d'un instance d'un objet GRIBImageIO utilisant les object factory.
 */
class GRIBImageIOFactory : public itk::ObjectFactoryBase
{
public:
  /** Standard class typedefs. */
  typedef GRIBImageIOFactory   Self;
  typedef itk::ObjectFactoryBase  Superclass;
  typedef itk::SmartPointer<Self>  Pointer;
  typedef itk::SmartPointer<const Self>  ConstPointer;

  /** Class methods used to interface with the registered factories. */
  virtual const char* GetITKSourceVersion(void) const;
  virtual const char* GetDescription(void) const;

  /** Method for class instantiation. */
  itkFactorylessNewMacro(Self);

  /** Run-time type information (and related methods). */
  itkTypeMacro(GRIBImageIOFactory, itk::ObjectFactoryBase);

  /** Register one factory of this type  */
  static void RegisterOneFactory(void)
  {
    GRIBImageIOFactory::Pointer GRIBFactory = GRIBImageIOFactory::New();
    itk::ObjectFactoryBase::RegisterFactory(GRIBFactory);
  }

protected:
  GRIBImageIOFactory();
  virtual ~GRIBImageIOFactory();

private:
  GRIBImageIOFactory(const Self&); //purposely not implemented
  void operator=(const Self&); //purposely not implemented

};


} // end namespace vns

#endif
